IF SCHEMA_ID(N'Games') IS NULL
   EXEC(N'CREATE SCHEMA Games;');
GO

DROP TABLE IF EXISTS Games.Review
DROP TABLE IF EXISTS Games.GameConsole
GO
DROP TABLE IF EXISTS Games.Console
DROP TABLE IF EXISTS Games.GameDeveloper
GO
DROP TABLE IF EXISTS Games.OrderLine
DROP TABLE IF EXISTS Games.[Order]
DROP TABLE IF EXISTS Games.Customer
DROP TABLE IF EXISTS Games.Developer
GO
DROP TABLE IF EXISTS Games.Game
GO

CREATE TABLE Games.Game (
	GameID INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	Genre NVARCHAR(50) NOT NULL,
	GameName NVARCHAR(150) NOT NULL
);
GO

CREATE TABLE Games.Customer (
	CustomerID INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	Email NVARCHAR(128) NOT NULL UNIQUE,
	CustomerFirstName NVARCHAR(50) NOT NULL,
	CustomerLastName NVARCHAR(50) NOT NULL
);
GO

CREATE TABLE Games.[Order] (
	OrderID INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	CustomerID INT FOREIGN KEY REFERENCES Games.Customer(CustomerID),
	OrderDate DATE DEFAULT GETDATE() NOT NULL

);
GO

CREATE TABLE Games.OrderLine (
	OrderLineID INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	GameID INT FOREIGN KEY REFERENCES Games.Game(GameID) NOT NULL,
	OrderID INT FOREIGN KEY REFERENCES Games.[Order](OrderID) NOT NULL,
	Price DECIMAL(5, 2) NOT NULL
);
GO

CREATE TABLE Games.Review (
	ReviewID INT IDENTITY(1,1) NOT NULL UNIQUE,
	GameID INT REFERENCES Games.Game(GameID) NOT NULL,
	CustomerID INT REFERENCES Games.Customer(CustomerID) NOT NULL,
	ReviewScore INT CHECK (ReviewScore BETWEEN 1 AND 10),
	PRIMARY KEY(GameID, CustomerID)

);
GO

CREATE TABLE Games.Console (
	ConsoleID INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	ConsoleName NVARCHAR(30) NOT NULL UNIQUE
);
GO

CREATE TABLE Games.GameConsole  (
	GameID INT REFERENCES Games.Game(GameID) NOT NULL,
	ConsoleID INT REFERENCES Games.Console(ConsoleID) NOT NULL,
	PRIMARY KEY(GameID, ConsoleID)
	
);
GO

CREATE TABLE Games.Developer (
	DeveloperID INT IDENTITY(1,1) PRIMARY KEY NOT NULL,
	DeveloperName NVARCHAR(100) UNIQUE,
	DeveloperCity NVARCHAR(50),
	DeveloperCountry NVARCHAR(50)
);
GO

CREATE TABLE Games.GameDeveloper (
	GameID INT REFERENCES Games.Game(GameID) NOT NULL,
	DeveloperID INT REFERENCES Games.Developer(DeveloperID) NOT NULL,
	PRIMARY KEY (GameID, DeveloperID)
);
GO

