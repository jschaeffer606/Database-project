DROP PROCEDURE IF EXISTS DeveloperTotalSales
DROP PROCEDURE IF EXISTS AverageGameRatings
GO
CREATE PROCEDURE DeveloperTotalSales
AS
SELECT DeveloperName, SUM(OrderLine.Price) AS TotalSales
FROM Games.Developer
INNER JOIN Games.GameDeveloper ON GameDeveloper.DeveloperID = Developer.DeveloperID
INNER JOIN Games.Game ON Game.GameID = GameDeveloper.GameID
INNER JOIN Games.OrderLine ON OrderLine.GameID = Game.GameID
GROUP BY DeveloperName
GO

CREATE PROCEDURE AverageGameRatings
AS
SELECT GameName, Genre, AVG(ReviewScore) AS AverageReviewScore, DENSE_RANK() OVER(PARTITION BY Genre ORDER BY AVG(ReviewScore) DESC) AS [Rank]
FROM Games.Game
INNER JOIN Games.Review ON Review.GameID = Game.GameID
GROUP BY GameName, Genre
ORDER BY Genre, [Rank], AVG(ReviewScore)
GO

CREATE PROCEDURE HighestSales  
AS
SELECT TOP(15) PERCENT GameName, SUM(OrderLine.Price) AS Sales, DENSE_RANK() OVER(ORDER BY SUM(OrderLine.Price) DESC) AS [Rank]
FROM Games.Game
INNER JOIN Games.OrderLine ON OrderLine.GameID = Game.GameID
GROUP BY GameName
ORDER BY [Rank]
GO

CREATE PROCEDURE NumberOfCustomers
AS
SELECT GameName, COUNT(DISTINCT CustomerID) AS NumberOfCustomers
FROM Games.Game
INNER JOIN Games.OrderLine ON OrderLine.GameID = Game.GameID
INNER JOIN Games.[Order] ON [Order].OrderID = OrderLine.OrderID
GROUP BY GameName
GO




